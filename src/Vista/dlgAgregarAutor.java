    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        CAutor contAut=new CAutor();
        try {
            if (contAut.agregarAutor(txtnombre, txtapellido))
            {
                JOptionPane.showMessageDialog(this,"Autor Agregado Correctamente","Autor Agregado",JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"El Autor  no se pudo agregar","Autor  No Agregado",JOptionPane.ERROR_MESSAGE);
                dispose();
            }
        } catch (SQLException ex) {
            Logger.getLogger(dlgAgregarAutor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(dlgAgregarAutor.class.getName()).log(Level.SEVERE, null, ex);
        }
                  
    }//GEN-LAST:event_btnGuardarActionPerformed

