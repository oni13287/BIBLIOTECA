    private void activarDialogo(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_activarDialogo
       try {
            
            txt_id.setText(idAutor );
            CAutor cEmp= new CAutor();
            Object [] datosAutor =cEmp.pedirAutor(idAutor );
            txtnombre.setText(datosAutor[0].toString());
            txtapellido.setText(datosAutor[1].toString());
            
        } catch (SQLException ex) {
            Logger.getLogger(dlgEditarAutor.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_activarDialogo

