    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
      try {
          CAutor contAut=new CAutor();
          if (contAut.editarAutor( txt_id, txtnombre, txtapellido))
          {
              JOptionPane.showMessageDialog(this,"Autor Actualizado Correctamente","Autor Actualizado",JOptionPane.INFORMATION_MESSAGE);
              dispose();
          }
          else
          {
              JOptionPane.showMessageDialog(this,"El Autor  no se pudo Actualizar","Autor  No Actualizado",JOptionPane.ERROR_MESSAGE);
              dispose();
          }
      } catch (SQLException ex) {
          Logger.getLogger(dlgEditarAutor.class.getName()).log(Level.SEVERE, null, ex);
      } catch (ParseException ex) {
          Logger.getLogger(dlgEditarAutor.class.getName()).log(Level.SEVERE, null, ex);
      }
    }//GEN-LAST:event_btnGuardarActionPerformed

