    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
      try {
          CCategoria contCat=new CCategoria();
          if (contCat.editarCategoria( txt_id, txtnombre))
          {
              JOptionPane.showMessageDialog(this,"Categoria Actualizada Correctamente","Categoria Actualizada",JOptionPane.INFORMATION_MESSAGE);
              dispose();
          }
          else
          {
              JOptionPane.showMessageDialog(this,"La Categoria  no se pudo Actualizar","Categoria No Actualizada",JOptionPane.ERROR_MESSAGE);
              dispose();
          }
      } catch (SQLException ex) {
          Logger.getLogger(dlgEditarCategoria.class.getName()).log(Level.SEVERE, null, ex);
      } catch (ParseException ex) {
          Logger.getLogger(dlgEditarCategoria.class.getName()).log(Level.SEVERE, null, ex);
      }
    }//GEN-LAST:event_btnGuardarActionPerformed

