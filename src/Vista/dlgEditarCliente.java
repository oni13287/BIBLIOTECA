    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            // TODO add your handling code here:
            CCliente contCli=new CCliente();
            if (contCli.editarCliente( txt_id, txtnombre, txtapellido, cmbgenero,txtemail, txtdireccion))
            {
                JOptionPane.showMessageDialog(this,"Cliente Actualizado Correctamente","Cliente  Actualizado",JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"El Cliente  no se pudo Actualizar","Cliente  No Actualizado",JOptionPane.ERROR_MESSAGE);
                dispose();
            }
        } catch (ParseException ex) {
            Logger.getLogger(dlgEditarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void activarDialogo(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_activarDialogo
       try {
            
            txt_id.setText(idCliente);
            CCliente cCli= new CCliente();
            Object [] datosCliente =cCli.pedirCliente (idCliente );
            txtnombre.setText(datosCliente[0].toString());
            txtapellido.setText(datosCliente[1].toString());
            cmbgenero.setSelectedIndex(Integer.parseInt(datosCliente[2].toString()));
            txtemail.setText(datosCliente[3].toString());
            txtdireccion.setText(datosCliente[4].toString());
                        
        } catch (SQLException ex) {
            Logger.getLogger(dlgEditarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_activarDialogo

