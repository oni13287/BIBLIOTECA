    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            // TODO add your handling code here:
            CEmpleado contEmp=new CEmpleado();
            if (contEmp.editarEmpleado( txt_id, txtnombre, txtapellido,txtfecha, cmbgenero,txtemail, txtdireccion))
            {
                JOptionPane.showMessageDialog(this,"Empleado Actualizado Correctamente","Empleado  Actualizado",JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"El Empleado  no se pudo Actualizar","Empleado  No Actualizado",JOptionPane.ERROR_MESSAGE);
                dispose();
            }
        } catch (ParseException ex) {
            Logger.getLogger(dlgEditarEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void activarDialogo(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_activarDialogo
       try {
            
            txt_id.setText(idEmpleado );
            CEmpleado cEmp= new CEmpleado();
            Object [] datosEmpleado =cEmp.pedirEmpleado (idEmpleado );
            txtnombre.setText(datosEmpleado[0].toString());
            txtapellido.setText(datosEmpleado[1].toString());
            cmbgenero.setSelectedIndex(Integer.parseInt(datosEmpleado[3].toString()));
            txtemail.setText(datosEmpleado[4].toString());
            txtdireccion.setText(datosEmpleado[5].toString());
                        
        } catch (SQLException ex) {
            Logger.getLogger(dlgEditarEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_activarDialogo

