    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            CLibro contLib=new CLibro();
            if (contLib.editarLibro(txt_id, txtnombre, txtautor, txtcategoria, txtprestamo))
            {
                JOptionPane.showMessageDialog(this,"Libro Actualizado Correctamente","Libro  Actualizado",JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"El Libro no se pudo Actualizar","Libro  No Actualizado",JOptionPane.ERROR_MESSAGE);
                dispose();
            }
        } catch (SQLException ex) {
            Logger.getLogger(dlgEditarLibro.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(dlgEditarLibro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void activarDialogo(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_activarDialogo
       try {
            
            txt_id.setText(idLibro );
            CLibro cLib= new CLibro();
            Object [] datosLibro =cLib.pedirLibro (idLibro );
            txtnombre.setText(datosLibro[0].toString());
            txtautor.setText(datosLibro[1].toString());
            txtcategoria.setText(datosLibro[2].toString());
            txtprestamo.setText(datosLibro[3].toString());
                        
        } catch (SQLException ex) {
            Logger.getLogger(dlgEditarLibro.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_activarDialogo

